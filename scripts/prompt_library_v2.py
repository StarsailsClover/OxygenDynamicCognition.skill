def get_prompt_v2(level: str, question: str, context: str = "") -> tuple:
    if level not in COGNITIVE_LEVELS_V2:
        raise ValueError(f"Unknown cognitive level: {level}")

    level_config = COGNITIVE_LEVELS_V2[level]
    system_prompt = level_config["system_prompt"]

    if level in ["L3", "L4", "L5"] and context:
        user_prompt = level_config["user_template"].format(
            question=question,
            context_block=f"\n\n【上下文信息（仅供参考）】\n{context}\n",
        )
    else:
        user_prompt = level_config["user_template"].format(
            question=question,
            context_block="",
        )

    return system_prompt, user_prompt


def get_level_config_v2(level: str) -> dict:
    if level not in COGNITIVE_LEVELS_V2:
        raise ValueError(f"Unknown cognitive level: {level}")
    return COGNITIVE_LEVELS_V2[level]


def next_level_v2(current_level: str) -> str:
    levels = ["L1", "L2", "L3", "L4", "L5"]
    if current_level not in levels:
        return "L2"
    current_idx = levels.index(current_level)
    if current_idx < len(levels) - 1:
        return levels[current_idx + 1]
    return "L5"


def classify_problem_type(question: str) -> str:
    """根据问题内容自动分类问题类型"""
    question_lower = question.lower()
    for ptype, keywords in PROBLEM_TYPE_CLASSIFIER.items():
        for kw in keywords:
            if kw in question_lower:
                return ptype
    return "综合类"
